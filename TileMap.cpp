Map::Map()
{
	for (int i = 0; i < 20; i++)
	{
		for (int j = 0; j < 25; j++)
		{
			vec2 destination = vec2(0, 0);
			destination.y = i * TILE_SIZE;
			destination.x = j * TILE_SIZE;
			map[i][j] = new Tile(destination);
		}
	}
	LoadMap(default_level);
}

void Map::LoadMap(int a[20][25])
{
	int type = 0;
	vec2 destination = vec2(0, 0);

	for (int i = 0; i < 20; i++)
	{
		for (int j = 0; j < 25; j++)
		{
			destination.x = i * TILE_SIZE;
			destination.y = j * TILE_SIZE;
			type = a[i][j];
			if (type == 1)
			{
				map[i][j]->setRect(TILE_SIZE, TILE_SIZE);
				map[i][j]->setReal();
			}
			else
			{
				//crutch
				map[i][j]->setFake();
			}
			
		}
		//cout << endl;
	}
}

void Map::DrawMap(Surface* screen)
{
	for (int i = 0; i < 20; i++)
	{
		for (int j = 0; j < 25; j++)
		{
			if (map[i][j]->getReal())
				map[i][j]->Draw(screen);
		}
	}
}